package JUnit;

public class Calculation {

	int add(int value1, int value2) {

		return value1 + value2;

	}

	int subtract(int value1, int value2) {

		return value1 - value2;
}

	int multiply(int value1, int value2) {

		return value1 * value2;

	}

	double divide(int value1, int value2) {

		return value1 / value2;

	}

}
